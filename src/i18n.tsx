const MESSAGES = {
  en: EN_MESSAGES,
  es: ES_MESSAGES,
} as const satisfies Record<AppLocale, Record<TranslationKey, string>>;

interface I18nContextValue {
  locale: AppLocale;
  setLocale: (locale: SupportedAppLocale) => void;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

function interpolate(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) =>
    vars[key] === undefined ? `{${key}}` : String(vars[key])
  );
}

export function I18nProvider({ children }: PropsWithChildren) {
  const [locale, setLocaleState] = useState<AppLocale>(() => getAppLocale());

  useEffect(() => {
    const syncLocale = () => setLocaleState(getAppLocale());
    window.addEventListener("tama-config-changed", syncLocale);
    return () => window.removeEventListener("tama-config-changed", syncLocale);
  }, []);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale: (nextLocale) => setAppLocale(nextLocale),
      t: (key, vars) => interpolate(MESSAGES[locale][key] ?? MESSAGES.en[key] ?? key, vars),
    }),
    [locale]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within an I18nProvider");
  }
  return context;
}
